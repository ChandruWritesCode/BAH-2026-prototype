import ee
import pandas as pd
import requests
import argparse
from io import StringIO

def initialize_gee(project_id):
    ee.Initialize(project=project_id)

def create_training_data(lat, lon, buffer_meters):
    print("Connecting to GEE and building multi-band image...")
    roi = ee.Geometry.Point([lon, lat]).buffer(buffer_meters)
    
    # 1. Landsat 8 - Land Surface Temperature
    # Widened date range to ensure satellite flyovers and cloud-free pixels
    landsat = ee.ImageCollection("LANDSAT/LC08/C02/T1_L2") \
        .filterBounds(roi) \
        .filterDate('2026-03-01', '2026-04-30') \
        .median()
    lst = landsat.select('ST_B10').multiply(0.00341802).add(149.0).subtract(273.15).rename('LST_Celsius')
    
    # 2. Sentinel 2 - Green Spaces (NDVI) and Cool Roof proxy (Albedo/Reflectance)
    sentinel = ee.ImageCollection("COPERNICUS/S2_SR_HARMONIZED") \
        .filterBounds(roi) \
        .filterDate('2026-03-01', '2026-04-30') \
        .median()
    
    ndvi = sentinel.normalizedDifference(['B8', 'B4']).rename('NDVI')
    
    # Simplified broadband albedo approximation using visible and NIR bands
    albedo = sentinel.expression(
        '(B2 + B3 + B4 + B8) / 40000', {
            'B2': sentinel.select('B2'),
            'B3': sentinel.select('B3'),
            'B4': sentinel.select('B4'),
            'B8': sentinel.select('B8')
        }
    ).rename('Albedo')

    # 3. Combine all features into one image
    combined_image = ee.Image([lst, ndvi, albedo])

    # 4. Sample the pixels within our region at 30-meter resolution
    print("Sampling pixels (this may take a minute)...")
    sampled_points = combined_image.sample(
        region=roi, 
        scale=30, 
        geometries=True,
        numPixels=100000 # Safety limit for our 5km buffer
    )

    # 5. Export to CSV via a temporary download URL (bypasses memory limits)
    print("Generating download URL...")
    url = sampled_points.getDownloadURL(filetype='CSV')
    
    print("Downloading and processing data into Pandas...")
    response = requests.get(url)
    df = pd.read_csv(StringIO(response.text))
    
    # Clean up the system index column generated by GEE
    if 'system:index' in df.columns:
        df = df.drop(columns=['system:index'])
        
    # Save the final tabular dataset
    csv_filename = "bengaluru_heat_dataset.csv"
    df.to_csv(csv_filename, index=False)
    print(f"Success! Dataset saved as {csv_filename} with {len(df)} rows.")

if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--project", required=True, help="Google Cloud Project ID")
    parser.add_argument("--lat", type=float, required=True, help="Latitude")
    parser.add_argument("--lon", type=float, required=True, help="Longitude")
    parser.add_argument("--buffer", type=int, default=5000, help="Buffer radius in meters")
    
    args = parser.parse_args()
    initialize_gee(args.project)
    create_training_data(args.lat, args.lon, args.buffer)